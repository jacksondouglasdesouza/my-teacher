function prof001(){
    event.preventDefault();

    let price = document.getElementById("price").value;

    let hours = document.getElementById("hours").value;

    let total = price * hours;

    if(hours > 0 && hours <= 8){
        document.getElementById("pag-01").innerHTML = "Você vai pagar: R$ " + total;
    }
    else
    {
        document.getElementById("pag-01").innerHTML = "Insira valor entre 1 e 8";
    }

}

